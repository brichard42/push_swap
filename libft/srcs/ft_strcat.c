char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		k;

	i = 0;
	while (s1[i])
		i++;
	k = 0;
	while (s2[k])
	{
		s1[i + k] = s2[k];
		k++;
	}
	s1[i + k] = '\0';
	return (s1);
}
